type MapTargetPromptInput = {
  mapType: string;
  targetId: string;
};

const MAP_TYPE_GUIDANCE: Record<string, string> = {
  country:
    "Это объект типа country. Нужен простой детский рассказ о стране. Расскажи где она находится, чем известна, какие там природа, города, люди или традиции.",
  river:
    "Это объект типа river. Нужен простой детский рассказ о реке. Расскажи где она течет, почему важна, какие животные, города или природные явления с ней связаны.",
  sea: "Это объект типа sea. Нужен простой детский рассказ о море. Расскажи где оно находится, какое оно, кто в нем живет и почему оно важно людям.",
  physic:
    "Это объект типа physic. Нужен простой детский рассказ о природном объекте или чуде природы. Расскажи где он находится, как выглядит и чем удивляет.",
  flag:
    "Это объект типа flag. Нужен простой детский рассказ о стране, чей флаг показан. Коротко объясни страну, ее особенности и что ребенок может о ней запомнить.",
  animal:
    "Это объект типа animal. Нужен простой детский рассказ о животном или биоме. Если target_id похож на биом или природную зону, расскажи о ней, какие там животные и климат. Если это животное, расскажи как оно выглядит, где живет и что умеет.",
  culture:
    "Это объект типа culture. Нужен простой детский рассказ о культурной особенности, традиции, музыке, одежде, празднике или известном символе страны или региона.",
  weather:
    "Это объект типа weather. Нужен простой детский рассказ о климате, погодной зоне или природных условиях. Объясни погоду простыми словами и с понятными примерами.",
  food:
    "Это объект типа food. Нужен простой детский рассказ о еде, блюде или кулинарной традиции. Расскажи из чего это делают, где любят есть и чем это интересно.",
};

export function humanizeMapTargetId(targetId: string): string {
  return targetId
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function buildMapTargetStoryPrompt(input: MapTargetPromptInput): string {
  const mapType = input.mapType.trim().toLowerCase();
  const targetId = input.targetId.trim();
  const readableTarget = humanizeMapTargetId(targetId);
  const guidance =
    MAP_TYPE_GUIDANCE[mapType] ??
    "Нужен простой детский познавательный рассказ по теме объекта карты.";

  return [
    "Ты пишешь короткие детские тексты для уроков по карте.",
    guidance,
    `map_type: ${mapType}`,
    `target_id: ${targetId}`,
    `Читаемое имя объекта: ${readableTarget}`,
    "",
    "Требования к тексту:",
    "- Пиши только на русском языке.",
    "- Текст должен быть фактическим и понятным ребенку 6-10 лет.",
    "- Дай 6-8 коротких предложений.",
    "- Каждое предложение должно быть простым и самостоятельным.",
    "- Используй только обычные точки и запятые.",
    "- Не используй списки, кавычки, двоеточия, точки с запятой, скобки, эмодзи и markdown.",
    "- Не начинай с вводных фраз вроде 'Конечно' или 'Давай узнаем'.",
    "- Текст должен легко делиться на слайды по 1-2 предложения.",
    "- Добавь подробные, но простые факты по теме объекта.",
    "- Не выдумывай редкие или сомнительные факты.",
    "",
    'Верни только JSON объекта вида {"content":"..."}',
    "- Поле content обязательно.",
    "- В content не вставляй переносы строк.",
    "- Никакого текста вне JSON.",
  ].join("\n");
}
